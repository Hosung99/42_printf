#include "ft_printf.h"

int	format_c(va_list arg, int *cnt)
{
	char	c;

	c = va_arg(arg, int);
	if (write(1, &c, 1) == -1)
		return (-1);
	*cnt = *cnt + 1;
	return (1);
}
